package IntelligentLightSystemApplication.Room;

class LightSource{
    // light-source's coordinates in the room
    private double x;
    private double y;
    private double z;

   LightSource(double x, double y,double z){
        this.x=x;
        this.y=y;
        this.z=z;
    }

    public double getx(){
        return x;
    }

    public double getz(){
        return x;
    }

    public double gety(){
        return x;
    }
}
